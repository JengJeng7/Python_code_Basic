# ตัวดำเนินการทางคณิตศาสตร์

# ประกอบไปด้วย ตัวดำเนินการ(Operator) 
#             ตัวที่ถูกดำเนินการ(Operand) 

#ตัวอย่าง
# X + Y
# A , Y  => ตัวถูกดำเนินการ (Operand)
# + => ตัวดำเนินการ (Operator)

x = 5
y = 3

print ("ผลบวก ", x+y)
print ("ผลลบ", x-y)
print ("ผลคูณ", x*y)
print ("ผลหาร", x/y)
print ("หารปัดเศษ", x//y)
print ("ยกกำลัง", x**y)
print ("การหารเอาเศษ", x%y) 

# เครื่องหมาย คอมม่า (,) เป็นเครื่องหมายคั่น ระหว่าง String กับ ตัวแปล