# Compound Assignment ตัวดำเนินการที่ใช้อัพเดตหรือแก้ไขตัวแปล

# เช่น 
# x=10 (ก่อนเพิ่มค่า)

# การเพิ่มค่า 
# x=x+10
# ถ้าเขียนอยู่ในรูป Compound Assignment
# x+=10, x/=10, x*=10, x-=10, x**=10 (** = ยกกำลัง), x%=10 (% =หารเอาเศษ)

x=10
print("ก่อนเพิ่มค่า = ",x)

x=x+10 
print("หลังเพิ่มค่า = ",x)

#รูปแบบ Compound Assignment
x*=10 # x=x*10   (* = คูณ)
print("หลังเพิ่มค่าอีกทีหนึ่ง = ",x)
